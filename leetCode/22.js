/**
 * @param {number} n
 * @return {string[]}
 */


function generateParenthesis(n) {
    const result = [];
  
    function backtrack(current, left, right) {
      if (left === 0 && right === 0) {
        result.push(current);
        return;
      }
  
      if (left > 0) {
        backtrack(current + '(', left - 1, right);
      }
  
      if (right > left) {
        backtrack(current + ')', left, right - 1);
      }
    }
  
    backtrack('', n, n);
    return result;
  }
  
  // Пример использования
  const n = 3;
  const combinations = generateParenthesis(n);
